function defaultLibraryBlockUserData = getDefaultLibraryBlockUserData(blockTitle)
% GETDEFAULTLIBRARYBLOCKUSERDATA returns the text used by default in the 
%   UserData of the blocks in the SDD Blocks Library.
%
%   Inputs:
%       blockTitle                  The name of the DocBlock (or text file)
%                                   that we want UserData for (UserData is 
%                                   essentially the contents of the block).
%                                   This is also used to determine how to 
%                                   set the block's UserData and 
%                                   Description. Its value should be in the
%                                   following list:
%                                   {'Purpose','Internal Design',
%                                   'Rationale',
%                                   'Requirements Specification',
%                                   'Anticipated Changes',
%                                   'Document Purpose',
%                                   'Scope','General Definitions',
%                                   'General Acronyms','General Notation',
%                                   'System Definitions','System Acronyms',
%                                   'System Notation'}
%
%   Outputs:
%       defaultLibraryBlockUserData     Summarizes syntax which can be used
%                                       in Text DocBlocks and text files
%                                       that are included in reports
%                                       generated by SDD_Report.rpt.
%                                       Returns an empty string for an 
%                                       unexpected blockTitle.

[libraryBlockDescription, specificComments] = getLibraryBlockDescription(blockTitle);
if ~(isempty(libraryBlockDescription))% && isempty(libraryBlockDescription)) %<- that looks like it was a typo and should be safe to remove
    defaultLibraryBlockUserData = sprintf([...
        '%% ', strrep(libraryBlockDescription, '\n', '\n%% '), ...
        strrep(specificComments, '\n', '\n%% '), ...
        '\n%% ---------------Summary of recognized syntax for this text----------------\n', ...
        '%% Lines leading with a "%%" will be ignored when the report is generated.\n', ...
        '%%\n', ...
        '%% Creating Links:\n', ...
        '%%   [goto=myLinkID]Clicking this text would move the cursor in the report to the anchor with a LinkID of "myLinkID".[/goto]\n', ...
        '%%   [anchor=myLinkID]The above link would lead to here.[/anchor]\n', ...
        '%%   [url=externalLink]Clicking this text will open an external link, externalLink. (E.g. http://www.mathworks.com/)[/url]\n', ...
        '%%\n', ...
        '%% Adding Images:\n', ...
        '%%   [image=imagePath]imageTitle[/image]\n', ...
        '%%\n', ...
        '%% When you want a new line in the report hit enter twice (a single new line in this document is ignored for readability purposes).\n', ...
        '%%\n', ...
        '%% Any of the lines you see here by default may be removed.\n']);
else
    defaultLibraryBlockUserData = '';
end
end