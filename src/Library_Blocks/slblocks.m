function blkStruct = slblocks
% SLBLOCKS specifies that the library "SDD_Blocks" should appear in
% the Library Browser as "SDD Blocks" and be cached in the browser
% repository.

Browser.Library = 'SDD_Blocks';
% 'SDD_Blocks' is the name of the library

Browser.Name = 'SDD Blocks';
% 'SDD Blocks' is the library name that appears in the Library Browser

blkStruct.Browser = Browser;